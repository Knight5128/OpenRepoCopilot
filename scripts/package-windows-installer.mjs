#!/usr/bin/env node
import { spawnSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const releaseRoot = path.join(repoRoot, "release");
const installerWorkRoot = path.join(releaseRoot, "windows-installer");
const appVersion = readPackageVersion();
const args = new Set(process.argv.slice(2));
const dryRun = args.has("--dry-run");

main();

function main() {
  if (process.platform !== "win32") {
    throw new Error("Windows installer packaging must run on Windows.");
  }

  const before = listPackageDirs();
  run("pnpm", ["package:app"]);
  const stageDir = findNewStageDir(before);
  const packageName = path.basename(stageDir);
  const timestamp = packageName.replace(/^openrepo-copilot-app-/, "");
  const outputBaseName = `OpenRepoCopilot-Setup-${timestamp}`;
  const outputDir = releaseRoot;
  const issPath = path.join(installerWorkRoot, `${outputBaseName}.iss`);

  fs.mkdirSync(installerWorkRoot, { recursive: true });
  writeInnoScript({ stageDir, issPath, outputDir, outputBaseName });

  console.log(`Windows installer script: ${path.relative(repoRoot, issPath)}`);

  if (dryRun) {
    console.log("Dry run enabled; skipping Inno Setup compiler.");
    return;
  }

  const iscc = findInnoCompiler();
  run(iscc, [issPath]);

  const installerPath = path.join(outputDir, `${outputBaseName}.exe`);
  if (!fs.existsSync(installerPath)) {
    throw new Error(`Expected installer not found: ${installerPath}`);
  }

  console.log("");
  console.log(`Windows installer: ${path.relative(repoRoot, installerPath)}`);
}

function readPackageVersion() {
  const packageJson = JSON.parse(fs.readFileSync(path.join(repoRoot, "package.json"), "utf8"));
  return typeof packageJson.version === "string" ? packageJson.version : "0.1.0";
}

function listPackageDirs() {
  if (!fs.existsSync(releaseRoot)) return new Set();
  return new Set(
    fs
      .readdirSync(releaseRoot, { withFileTypes: true })
      .filter((entry) => entry.isDirectory() && /^openrepo-copilot-app-\d{8}T\d{6}Z$/.test(entry.name))
      .map((entry) => entry.name),
  );
}

function findNewStageDir(before) {
  const after = [...listPackageDirs()];
  const created = after.filter((name) => !before.has(name)).sort();
  const selected = created.at(-1) ?? after.sort().at(-1);
  if (!selected) {
    throw new Error("Could not find the packaged app directory under release/.");
  }
  return path.join(releaseRoot, selected);
}

function writeInnoScript({ stageDir, issPath, outputDir, outputBaseName }) {
  ensureInside(issPath, installerWorkRoot);
  ensureInside(outputDir, releaseRoot);

  const content = `; Generated by scripts/package-windows-installer.mjs

[Setup]
AppId={{A2F638B0-5E4F-4F89-8F07-1F44B761E253}
AppName=OpenRepoCopilot
AppVersion=${appVersion}
AppPublisher=OpenRepoCopilot
DefaultDirName={localappdata}\\Programs\\OpenRepoCopilot
DefaultGroupName=OpenRepoCopilot
AllowNoIcons=yes
PrivilegesRequired=lowest
OutputDir=${innoValue(outputDir)}
OutputBaseFilename=${outputBaseName}
Compression=lzma2
SolidCompression=yes
WizardStyle=modern
UninstallDisplayIcon={app}\\scripts\\windows\\OpenRepoCopilot.cmd
SetupIconFile=${innoValue(path.join(stageDir, "assets", "openrepo-copilot-logo.ico"))}

[Tasks]
Name: "desktopicon"; Description: "Create a desktop shortcut"; GroupDescription: "Additional shortcuts:"; Flags: unchecked

[Files]
Source: "${innoValue(path.join(stageDir, "*"))}"; DestDir: "{app}"; Flags: ignoreversion recursesubdirs createallsubdirs

[Icons]
Name: "{group}\\OpenRepoCopilot"; Filename: "{app}\\scripts\\windows\\OpenRepoCopilot.cmd"; WorkingDir: "{app}"; IconFilename: "{app}\\assets\\openrepo-copilot-logo.ico"
Name: "{group}\\Uninstall OpenRepoCopilot"; Filename: "{uninstallexe}"
Name: "{autodesktop}\\OpenRepoCopilot"; Filename: "{app}\\scripts\\windows\\OpenRepoCopilot.cmd"; WorkingDir: "{app}"; IconFilename: "{app}\\assets\\openrepo-copilot-logo.ico"; Tasks: desktopicon

[Run]
Filename: "{app}\\scripts\\windows\\OpenRepoCopilot.cmd"; Description: "Launch OpenRepoCopilot"; Flags: postinstall skipifsilent nowait

[UninstallDelete]
Type: filesandordirs; Name: "{app}\\node_modules"
Type: filesandordirs; Name: "{app}\\understand-anything-plugin\\packages\\core\\node_modules"
Type: filesandordirs; Name: "{app}\\understand-anything-plugin\\packages\\dashboard\\node_modules"
Type: filesandordirs; Name: "{app}\\understand-anything-plugin\\packages\\openrepo\\node_modules"
`;

  fs.writeFileSync(issPath, content);
}

function findInnoCompiler() {
  if (process.env.INNO_SETUP_COMPILER) {
    return process.env.INNO_SETUP_COMPILER;
  }

  const fromPath = findCommandOnPath("ISCC.exe");
  if (fromPath) return fromPath;

  const candidates = [
    path.join(process.env.LOCALAPPDATA ?? "", "Programs", "Inno Setup 6", "ISCC.exe"),
    path.join(process.env["ProgramFiles(x86)"] ?? "C:\\Program Files (x86)", "Inno Setup 6", "ISCC.exe"),
    path.join(process.env.ProgramFiles ?? "C:\\Program Files", "Inno Setup 6", "ISCC.exe"),
  ].filter(Boolean);
  for (const candidate of candidates) {
    if (fs.existsSync(candidate)) return candidate;
  }

  throw new Error(
    "Inno Setup compiler not found. Install Inno Setup 6 and rerun pnpm package:app:win-installer, or set INNO_SETUP_COMPILER to ISCC.exe.",
  );
}

function findCommandOnPath(command) {
  const pathValue = process.env.Path ?? process.env.PATH ?? "";
  for (const dir of pathValue.split(path.delimiter)) {
    if (!dir) continue;
    const candidate = path.join(dir, command);
    if (fs.existsSync(candidate)) return candidate;
  }
  return undefined;
}

function run(command, args) {
  console.log(`> ${command} ${args.join(" ")}`);
  const result = spawnSync(command, args, {
    cwd: repoRoot,
    stdio: "inherit",
    shell: process.platform === "win32" && !path.isAbsolute(command),
    env: { ...process.env, FORCE_COLOR: "1" },
  });
  if (result.error) throw result.error;
  if (result.status !== 0) {
    throw new Error(`Command failed with exit code ${result.status}: ${command} ${args.join(" ")}`);
  }
}

function innoValue(value) {
  return value.replaceAll('"', '""');
}

function ensureInside(candidate, root) {
  const relative = path.relative(root, candidate);
  if (relative.startsWith("..") || path.isAbsolute(relative)) {
    throw new Error(`Refusing to write outside ${root}: ${candidate}`);
  }
}
